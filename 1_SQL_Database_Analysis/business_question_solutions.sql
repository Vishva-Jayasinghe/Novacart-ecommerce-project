--Q2 Calculate total revenue generated by each product category
--include % of contribution  of each category to total_revenue

SELECT c.category_name,
	   SUM(oi.total_sale) AS Total_per_cat,
	   (SUM(oi.total_sale) / (SELECT SUM(total_sale) FROM order_items) ) * 100 AS contribution
FROM  order_items oi
JOIN products p
ON oi.product_id = p.product_id
LEFT JOIN category c 
ON p.category_id = c.category_id
GROUP BY 1
ORDER BY 2 DESC;

