--Q4 Query to Monthly total sales over the past year.
--display sales trend ,grouping by month ,return current month_sale, last month sale

SELECT 
	Month,
	Year,
	COALESCE(total_sale,0) AS current_month_sale,
	COALESCE(LAG(total_sale,1) OVER (ORDER BY Year,Month),0) AS last_month_sale
FROM ---			
(
	SELECT
		EXTRACT(YEAR FROM o.order_date) AS Year,
		EXTRACT(MONTH FROM o.order_date) AS Month,
		ROUND(SUM(oi.total_sale ::numeric),3) AS total_sale
		
	FROM orders o 
	JOIN order_items oi 
	ON o.order_id = oi.order_id
	WHERE order_date >= CURRENT_DATE - INTERVAL '1 year'
	GROUP BY 1,2
	ORDER BY 2 ,1
) AS t1

	
