/*
Q18  Top 10 products with highest decreasing revenue ratio compare to last year(2024) and current year (2025)
return product_id , product_name,category name,previous year revenue and currenct year revenue decreasing ratio 
*/
WITH last_year_sale AS
(
	SELECT p.product_id,
		   p.product_name,
		   SUM(oi.total_sale) AS revenue	   
	FROM orders o
	JOIN order_items oi
		ON oi.order_id = o.order_id
	JOIN products p
		ON p.product_id = oi.product_id
	WHERE EXTRACT(YEAR FROM o.order_date) = 2024
	GROUP BY 1,2
)
,
current_year_sale
AS(
	SELECT     p.product_id,
			   p.product_name,
			   SUM(oi.total_sale) AS revenue	   
		FROM orders o
		JOIN order_items oi
			ON oi.order_id = o.order_id
		JOIN products p
			ON p.product_id = oi.product_id
		WHERE EXTRACT(YEAR FROM o.order_date) = 2025
		GROUP BY 1,2
)

SELECT cs.product_id,
	   ls.revenue AS last_year_revenue,
	   cs.revenue AS current_year_revenue,
	   ls.revenue - cs.revenue AS rev_diff,
	   ls.revenue - cs.revenue / ls.revenue * 100 AS rev_dec_ratio
	   
FROM last_year_sale ls
JOIN current_year_sale cs
ON ls.product_id = cs.product_id
WHERE ls.revenue > cs.revenue
ORDER BY 5 DESC
LIMIT 10;

/* 
Q19 Customer Retention Analysis
Identify customers who made repeat purchases within 30 days of their previous order.
*/
WITH customer_orders AS (
SELECT 
    o.customer_id,
    CONCAT(c.first_name,' ',c.last_name) AS customer_name,
    o.order_date AS first_order_date,
    LEAD(o.order_date) OVER(
        PARTITION BY o.customer_id 
        ORDER BY o.order_date
    ) AS next_order_date
FROM orders o
JOIN customers c
ON c.customer_id = o.customer_id
)

SELECT *,
       next_order_date - first_order_date AS days_between_orders
FROM customer_orders
WHERE next_order_date - first_order_date <= 30;

/*
Q20 Product Cross-Selling Analysis
Identify the top 5 product pairs that are most frequently purchased together in the same order.
*/
SELECT 
    p1.product_name AS product_1,
    p2.product_name AS product_2,
    COUNT(*) AS times_bought_together
FROM order_items oi1
JOIN order_items oi2
    ON oi1.order_id = oi2.order_id
    AND oi1.product_id < oi2.product_id
JOIN products p1
    ON oi1.product_id = p1.product_id
JOIN products p2
    ON oi2.product_id = p2.product_id
GROUP BY p1.product_name, p2.product_name
ORDER BY times_bought_together DESC
LIMIT 5;
